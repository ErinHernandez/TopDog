// Utility function to group picks by position
export const groupPicksByPosition = (picks, playerPool) => {
  const grouped = {
    QB: [],
    RB: [],
    WR: [],
    TE: [],
    FLEX: []
  };
  
  picks.forEach(pick => {
    const playerData = playerPool.find(p => p.name === pick);
    if (playerData && grouped[playerData.position]) {
      const playerInfo = {
        name: pick,
        position: playerData.position,
        team: playerData.team,
        bye: playerData.bye,
        adp: playerData.adp
      };
      
      // Add to position-specific group
      grouped[playerData.position].push(playerInfo);
      
      // Add RB, WR, and TE players to FLEX group
      if (['RB', 'WR', 'TE'].includes(playerData.position)) {
        grouped.FLEX.push(playerInfo);
      }
    }
  });
  
  // Sort each position by ADP
  Object.keys(grouped).forEach(pos => {
    grouped[pos].sort((a, b) => (a.adp || 999) - (b.adp || 999));
  });
  
  return grouped;
};
 